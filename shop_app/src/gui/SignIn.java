    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String email = jTextField1.getText();
        String password = String.valueOf(jPasswordField1.getPassword());

        String pattern = "^[\\w-\\.]+@([\\w-]+\\.)+[\\w-]{2,4}$";

        if (!email.matches(pattern)) {
            JOptionPane.showMessageDialog(this, "Please Enter A Valid Email Address", "WARNING", JOptionPane.WARNING_MESSAGE);

        } else if (password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Fill Passsword", "WARNING", JOptionPane.WARNING_MESSAGE);

        } else {
            String query = "SELECT * FROM employee WHERE email = '" + email + "' AND password = '" + password + "'";

            try {
                ResultSet result = Mysql.search(query);
                if (result.next()) {

                    String fname = result.getString("first_name");
                    String lname = result.getString("last_name");

                    Home window = new Home(email, fname, lname);
                    window.setVisible(true);
                    this.dispose();

                    setEmployeeEmail(email);

                } else {
                    JOptionPane.showMessageDialog(this, "Invalid Details", "WARNING", JOptionPane.WARNING_MESSAGE);
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

