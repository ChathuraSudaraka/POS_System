    private void calculate() {
        if (jFormattedTextField4.getText().isEmpty()) {
            discount = 0;
        } else {
            discount = Double.parseDouble(jFormattedTextField4.getText());
        }
        if (jFormattedTextField5.getText().isEmpty()) {
            payment = 0;
        } else {
            payment = Double.parseDouble(jFormattedTextField5.getText());
        }

        total = Double.parseDouble(jLabel5.getText());

        if (jCheckBox1.isSelected()) {
            withdrawPoints = true;
        } else {
            withdrawPoints = false;
        }

        paymentMethod = String.valueOf(jComboBox1.getSelectedItem());

        total -= discount;

        if (total < 0) {
            //discount error
        } else {
            //discount success
            if (withdrawPoints) {
                if (Double.parseDouble(jFormattedTextField5.getText()) == total) {
                    newPoints = 0;
                    total = 0;
                    //no payment required 
                } else if (Double.parseDouble(jFormattedTextField5.getText()) < total) {
                    newPoints = 0;
                    total -= Double.parseDouble(jFormattedTextField5.getText());
                    //payment required
                } else {
                    newPoints = Double.parseDouble(jFormattedTextField5.getText()) - total;
                    total = 0;
                    //no payment required
                }
            }
        }

        if (paymentMethod.equals("Cash")) {
            balance = payment - total;
            jFormattedTextField5.setEditable(true);

            if (balance < 0) {
                jButton3.setEnabled(false);
            } else {
                jButton3.setEnabled(true);
            }
        } else {
            //card payment
            payment = total;
            balance = 0;
            jFormattedTextField5.setText(String.valueOf(payment));
            jFormattedTextField5.setEditable(false);
        }
        jLabel8.setText(String.valueOf(balance));
    }

